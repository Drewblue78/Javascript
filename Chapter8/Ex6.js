// Use the split, join, and sort methods to write a function sortLetters that 
// rearranges the characters in a string so that they appear in lexicographic order.
//  For example, calling sortLetters("cabbage") should return the string "aabbceg".

function sortLetters(word) {
  var x = "word"
  var letters = []

  word.split(word)
  for (var l of x) {
    letters.push(1)
  }
  letters.sort()
  letters.join("")
}
